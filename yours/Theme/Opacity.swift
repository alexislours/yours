import SwiftUI

enum Opacity {
    /// Tinted icon and category backgrounds (e.g., `.background(color.opacity(iconBackground))`)
    static let iconBackground: Double = 0.12

    /// Subtle error/success fill backgrounds
    static let subtleBackground: Double = 0.08

    /// Borders, pills, and light overlays
    static let subtleBorder: Double = 0.15
}
