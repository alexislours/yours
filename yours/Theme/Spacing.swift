import Foundation

enum Spacing {
    /// 4pt:tight padding (pills, compact badges)
    static let xxs: CGFloat = 4

    /// 6pt:icon-to-label gaps, pill padding
    static let xs: CGFloat = 6

    /// 8pt:small gaps between related elements
    static let sm: CGFloat = 8

    /// 12pt:card internal gaps, section sub-spacing
    static let md: CGFloat = 12

    /// 16pt:standard content gaps
    static let lg: CGFloat = 16

    /// 18pt:card padding
    static let xl: CGFloat = 18

    /// 20pt:comfortable content padding
    static let xxl: CGFloat = 20

    /// 24pt:section spacing, screen horizontal padding
    static let xxxl: CGFloat = 24

    /// 32pt:large section dividers
    static let section: CGFloat = 32

    /// 40pt:generous vertical separation
    static let block: CGFloat = 40

    /// 48pt:screen-level padding, major sections
    static let screen: CGFloat = 48
}
